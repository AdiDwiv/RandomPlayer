import UIKit

extension UIColor {
    
    @nonobjc class var noir: UIColor {
        return UIColor(red: 17.0 / 255.0, green: 22.0 / 255.0, blue: 37.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var midnightPurple: UIColor {
        return UIColor(red: 52.0 / 255.0, green: 25.0 / 255.0, blue: 49.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var matins: UIColor {
        return UIColor(red: 87.0 / 255.0, green: 27.0 / 255.0, blue: 60.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var pinksx: UIColor {
        return UIColor(red: 122.0 / 255.0, green: 30.0 / 255.0, blue: 72.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var rose: UIColor {
        return UIColor(red: 157.0 / 255.0, green: 32.0 / 255.0, blue: 83.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var roseLite: UIColor {
        return UIColor(red: 185.0 / 255.0, green: 60.0 / 255.0, blue: 111.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var roseLitest: UIColor {
